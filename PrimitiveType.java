package mypack;

public class PrimitiveType {
	static boolean val1;
	static int val2;
	static float val3;
	static double val4;
	static String val5;
	static long val6;
	static char val7;
	
	public static void main(String[] args) {
		
		System.out.println("Default Values....");
		System.out.println("Value of Boolean = "+val1);
		System.out.println("Value of Int = "+val2);
		System.out.println("Value of Float = "+val3);
		System.out.println("Value of Double = "+val4);
		System.out.println("Value of String = "+val5);
		System.out.println("Value of Long = "+val6);
		System.out.println("Value of Char = "+val7);
		
	}
}
